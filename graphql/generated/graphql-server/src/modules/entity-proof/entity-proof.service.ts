import { Service } from 'typedi';
import { Repository } from 'typeorm';
import { InjectRepository } from 'typeorm-typedi-extensions';
import { BaseService, WhereInput } from 'warthog';

import { EntityProof } from './entity-proof.model';

@Service('EntityProofService')
export class EntityProofService extends BaseService<EntityProof> {
  constructor(@InjectRepository(EntityProof) protected readonly repository: Repository<EntityProof>) {
    super(EntityProof, repository);
  }

  async find<W extends WhereInput>(
    where?: any,
    orderBy?: string,
    limit?: number,
    offset?: number,
    fields?: string[]
  ): Promise<EntityProof[]> {
    let f = fields;
    if (f == undefined) {
      f = [];
    }

    return super.find<W>(where, orderBy, limit, offset, f);
  }
}
